    <p style="text-indent: 0ex; text-align: center">
        CHAPTER 4. PUBLIC EDUCATION MISSION, OBJECTIVES, AND GOALS
    </p>
    <p>
        Sec. 4.001. PUBLIC EDUCATION MISSION AND OBJECTIVES. (a) The mission of
        the public education system of this state is to ensure that all Texas
        children have access to a quality education that enables them to achieve
        their potential and fully participate now and in the future in the social,
        economic, and educational opportunities of our state and nation. That
        mission is grounded on the conviction that a general diffusion of
        knowledge is essential for the welfare of this state and for the
        preservation of the liberties and rights of citizens. It is further
        grounded on the conviction that a successful public education system is
        directly related to a strong, dedicated, and supportive family and that
        parental involvement in the school is essential for the maximum
        educational achievement of a child.
    </p>
    <p>(b) The objectives of public education are:</p>
    <p>
        OBJECTIVE 1: Parents will be full partners with educators in the education
        of their children.
    </p>
    <p>
        OBJECTIVE 2: Students will be encouraged and challenged to meet their full
        educational potential.
    </p>
    <p>
        OBJECTIVE 3: Through enhanced dropout prevention efforts, all students
        will remain in school until they obtain a high school diploma.
    </p>
    <p>
        OBJECTIVE 4: A well-balanced and appropriate curriculum will be provided
        to all students. Through that curriculum, students will be prepared to
        succeed in a variety of postsecondary activities, including employment and
        enrollment in institutions of higher education.
    </p>
    <p>
        OBJECTIVE 5: Educators shall cultivate in students an informed American
        patriotism and lead students in a close study of the founding documents of
        the United States and Texas. The purpose of this objective is to:
    </p>
    <p style="text-indent: 13ex">
        (1) increase students' knowledge of the deepest and noblest purposes of
        the United States and Texas;
    </p>
    <p style="text-indent: 13ex">
        (2) enhance students' intellectual independence so that students may
        become thoughtful, informed citizens who have an appreciation for the
        fundamental democratic principles of our state and national heritage; and
    </p>
    <p style="text-indent: 13ex">
        (3) guide students toward understanding and productively functioning in a
        free enterprise society.
    </p>
    <p>
        OBJECTIVE 6: Qualified and highly effective personnel will be recruited,
        developed, and retained.
    </p>
    <p>
        OBJECTIVE 7: The state's students will demonstrate exemplary performance
        in comparison to national and international standards.
    </p>
    <p>
        OBJECTIVE 8: School campuses will maintain a safe and disciplined
        environment conducive to student learning.
    </p>
    <p>
        OBJECTIVE 9: Educators will keep abreast of the development of creative
        and innovative techniques in instruction and administration using those
        techniques as appropriate to improve student learning.
    </p>
    <p>
        OBJECTIVE 10: Technology will be implemented and used to increase the
        effectiveness of student learning, instructional management, staff
        development, and administration.
    </p>
    <p>
        OBJECTIVE 11: The State Board of Education, the agency, and the
        commissioner shall assist school districts and charter schools in
        providing career and technology education to students.
    </p>
